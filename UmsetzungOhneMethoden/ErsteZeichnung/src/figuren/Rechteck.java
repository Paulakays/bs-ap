package figuren;

public class Rechteck {
    int positionX;
    int positionY;
    String farbe;
    int breite;
    int hoehe;

    public Rechteck(int positionX,int postionY,String farbe,int breite,int hoehe) {
        this.positionX = positionX;
        this.positionY = postionY;
        this.farbe = farbe;
        this.breite = breite;
        this.hoehe = hoehe;
    }
}



