package figuren;

public class Rechteck {
    int positionX;
    int positionY;
    int breite;
    String farbe;
    int hoehe;

    public Rechteck(int positionX, int positionY, int breite, String farbe, int hoehe) {
        this.positionX = positionX;
        this.positionY = positionY;
        this.breite = breite;
        this.farbe = farbe;
        this.hoehe = hoehe;
    }

}
